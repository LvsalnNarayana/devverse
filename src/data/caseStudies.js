// Extract all unique tags
export const allTags = [...new Set(caseStudies.flatMap((p) => p.tags))].sort();

/**
 * Page integration note:
 * - `src/pages/CaseStudies.jsx` passes `caseStudies`, `allTags`, and `categories`
 *   into `ModuleListingLayout`.
 * - `public/configs/modulePageLayouts.json` uses `optionsSource: "caseStudyCategories"`
 *   which is mapped to this `categories` array in the page wrapper.
 */

// Filter by category
export const filterByCategory = (category) => {
  if (!category || category === 'All') return caseStudies;
  return caseStudies.filter((p) => p.category === category);
};

// Filter by tag
export const filterByTag = (tag) => {
  if (!tag) return caseStudies;
  return caseStudies.filter((p) => p.tags.includes(tag));
};

// Filter by multiple tags (AND logic)
export const filterByTags = (tags) => {
  if (!tags || tags.length === 0) return caseStudies;
  return caseStudies.filter((p) => tags.every((tag) => p.tags.includes(tag)));
};

// Search projects
export const searchProjects = (query) => {
  if (!query) return caseStudies;
  const lowercaseQuery = query.toLowerCase();
  return caseStudies.filter(
    (p) =>
      p.title.toLowerCase().includes(lowercaseQuery) ||
      p.description.toLowerCase().includes(lowercaseQuery) ||
      p.tags.some((tag) => tag.toLowerCase().includes(lowercaseQuery)),
  );
};

// Sort projects
export const sortProjects = (projects, sortBy = 'title') => {
  const sorted = [...projects];
  switch (sortBy) {
    case 'title':
      return sorted.sort((a, b) => a.title.localeCompare(b.title));
    case 'category':
      return sorted.sort((a, b) => a.category.localeCompare(b.category));
    default:
      return sorted;
  }
};

// Get projects by category with count
export const getCategoryStats = () => {
  return categories.map((category) => ({
    category,
    count: filterByCategory(category).length,
  }));
};

// Get tag usage statistics
export const getTagStats = () => {
  return allTags
    .map((tag) => ({
      tag,
      count: filterByTag(tag).length,
    }))
    .sort((a, b) => b.count - a.count);
};

// Look up a single post by id (used by the inner blog detail route).
export const getBlogById = (id) => caseStudies.find((post) => post.id === id);
